    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
 String Fecha = txt_fecha.getText();      
 String Nombre = txt_nombre.getText();
        String Suma = txt_suma.getText();
        String Concepto = txt_concepto.getText();
        String TipoPago = (String) txt_tipoPago.getSelectedItem();
         
        if(Nombre.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega el Nombre");
        }
                
        else if(Suma.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega la Suma");
        }      
        else if(Concepto.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega el Concepto");
        }
        
        else if(TipoPago.equals("TIPO DE PAGO"))
        {
            JOptionPane.showMessageDialog(null, "Agrega Tipo de Pago");
        }
        else{
        PreparedStatement ps;
        String query = "INSERT INTO `recibo`(`Fecha`,`Nombre`,`Suma`, `Concepto`,`TipoPago`) VALUES (?,?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, Fecha);
            ps.setString(2, Nombre);
            ps.setString(3, Suma);            
            ps.setString(4, Concepto);
            ps.setString(5, TipoPago);


            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Nuevo Recibo Agregado");
                 
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Frm_Servicios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }           
        
        
        }}
    
        
