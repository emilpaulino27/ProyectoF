    private void btn_insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertarActionPerformed
        // TODO add your handling code here:

        String Nombre = txt_nombre.getText();
        String Apellido = txt_apellido.getText();
        String Cedula = txt_cedula.getText();
        String Telefono = txt_telefono.getText();
        String Direccion = txt_direccion.getText();
        String Cargo = txt_cargo.getText();

        if(Nombre.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega el Nombre del Empleado");
        }

        else if(Apellido.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega el Apellido del Empleado");
        }

        else if(Cedula.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega la Cédula del Empleado");
        }

        else if(Telefono.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega el Número de Teléfono del Empleado");
        }

        else if(Direccion.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega la Dirección del Empleado");
        }

        else if(Cargo.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega el Cargo del Empleado");
        }

        else if(revisarEmpleado(Cedula))
        {
            JOptionPane.showMessageDialog(null, "Este Número de Cédula Ya Existe");
        }
        else{
            PreparedStatement ps;
            String query = "INSERT INTO `empleados`(`Nombre`,`Apellido`, `Cedula`, `Telefono`, `Direccion`, `Cargo`) VALUES (?,?,?,?,?,?)";
            try {
                ps = MyConnection.getConnection().prepareStatement(query);

                ps.setString(1, Nombre);
                ps.setString(2, Apellido);
                ps.setString(3, Cedula);
                ps.setString(4, Telefono);
                ps.setString(5, Direccion);
                ps.setString(6, Cargo);

                if(ps.executeUpdate() > 0)
                {
                    JOptionPane.showMessageDialog(null, "Nuevo Empleaado Agregado");
                      Limpiar();
                                Mostrardatos("");
                }

            } catch (SQLException ex) {
                Logger.getLogger(Frm_empleados.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    }//GEN-LAST:event_btn_insertarActionPerformed

