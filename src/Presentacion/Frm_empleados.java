    public final void Mostrardatos(String valor){
            //Funcion para llenar la jtable de Usuarios desde la BD
            MyConnection cc=new MyConnection();
            Connection cn=MyConnection.getConnection();
            RefrescarTabla();
            Empleados.addColumn("ID Empleado");
            Empleados.addColumn("Nombre");
            Empleados.addColumn("Apellido");
            Empleados.addColumn("Cedula");
            Empleados.addColumn("Telefono");
            Empleados.addColumn("Direccion");
            Empleados.addColumn("Cargo");



            this.tbl_emp.setModel(Empleados);
            String sql;
            if (valor.equals(""))
            {
            sql="SELECT * FROM empleados";
            }
            else{
            sql="SELECT * FROM empleados WHERE Nombre like '%"+valor+"%'";
            } 

            String []datos=new String [7];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            
            datos[4]=rs.getString(5);
            datos[5]=rs.getString(6);
            datos[6]=rs.getString(7);



            Empleados.addRow(datos);
            }
            tbl_emp.setModel(Empleados);


            }catch(SQLException ex){
            Logger.getLogger(frm_Main.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    
    
    public boolean revisarUsuarios(String usuarios) {
            PreparedStatement ps;
            ResultSet rs;
            boolean revisar = false;
            String query = "SELECT * FROM `empleados` WHERE `Nombre` = ?";
            
            try {
                ps = MyConnection.getConnection().prepareStatement(query);
                ps.setString(1, usuarios);
                
                rs = ps.executeQuery();
                
                if (rs.next())
                {
                    revisar = true;
                }
            } catch (SQLException ex) {
            Logger.getLogger(Frm_empleados.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
        return false;
        }
