          public boolean revisarServicios(String servicios) {
            PreparedStatement ps;
            ResultSet rs;
            boolean revisar = false;
            String query = "SELECT * FROM `servicios` WHERE `Nombre_serv` = ?";
            
            try {
                ps = MyConnection.getConnection().prepareStatement(query);
                ps.setString(1, servicios);
                
                rs = ps.executeQuery();
                
                if (rs.next())
                {
                    revisar = true;
                }
            } catch (SQLException ex) {
            Logger.getLogger(Frm_Servicios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
        return false;
        }
        
          
          public final void Mostrardatos(String valor){
            //Funcion para llenar la jtable de Usuarios desde la BD
            MyConnection cc=new MyConnection();
            Connection cn=MyConnection.getConnection();
            RefrescarTabla();
            Servicios.addColumn("ID Servicio");
            Servicios.addColumn("Nombre Servicio");
            Servicios.addColumn("Descripci√≥n");
            Servicios.addColumn("Precio");
   
            this.tabla_servicio.setModel(Servicios);
            String sql;
            if (valor.equals(""))
            {
            sql="SELECT * FROM servicios";
            }
            else{
            sql="SELECT * FROM servicios WHERE Nombre_serv like '%"+valor+"%'";
            } 
          
            String []datos=new String [4];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            

            Servicios.addRow(datos);
            }
            tabla_servicio.setModel(Servicios);


            }catch(SQLException ex){
            Logger.getLogger(Frm_Servicios.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    
          
          
    private void btn_insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertarActionPerformed
        // TODO add your handling code here:
        
        String Nombre = txt_nombre.getText();
        String Descripcion = txt_descripcion.getText();
        String Precio = txt_precio.getText();
         
        if(Nombre.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega un Nombre del Servicio");
        }
        
        
        else if(Precio.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega el Precio del Servicio");
        }      
        else if(revisarServicios(Nombre))
        {
            JOptionPane.showMessageDialog(null, "Este Servicio ya existe");
        }
        else{
        PreparedStatement ps;
        String query = "INSERT INTO `servicios`(`Nombre_serv`,`Descripcion`, `Precio`) VALUES (?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, Nombre);
            ps.setString(2, Descripcion);
            ps.setString(3, Precio);

            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Nuevo Servicio Agregado");
                 Limpiar();
                Mostrardatos("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Frm_Servicios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }           
        
        
        }
    }//GEN-LAST:event_btn_insertarActionPerformed

  
